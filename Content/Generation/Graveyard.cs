        //place a giant dirt area for the graveyard to generate on
        private void PlaceArea(GenerationProgress progress, GameConfiguration configuration)
        {
            progress.Message = "Generating graveyard";

            int ClearHeight = 170;
            int XAmount = 0;

            //change height for y check based on world size
            if (Main.maxTilesX == 4200 && Main.maxTilesY == 1200) //small worlds
            {
                PositionY = (int)Main.worldSurface - 165;
                ClearHeight = 100;
                BiomeWidth = 400;
                XAmount = 0;
            }
            if (Main.maxTilesX == 6400 && Main.maxTilesY == 1800) //medium worlds
            {
                PositionY = (int)Main.worldSurface - 200;
                ClearHeight = 170;
                BiomeWidth = 450;
                XAmount = 100;
            }
            if (Main.maxTilesX == 8400 && Main.maxTilesY == 2400) //large worlds
            {
                PositionY = (int)Main.worldSurface - 275;
                ClearHeight = 230;
                BiomeWidth = 525;
                XAmount = 150;
            }

            //place biome based on opposite dungeon side
            if (Terraria.Main.dungeonX < Main.maxTilesX / 2)
			{
                PositionX = Main.maxTilesX - (800 + XAmount);
			}
			else
			{
                PositionX = 420;
			}

            int XStart = PositionX;
            int XEdge = XStart + BiomeWidth;

            bool foundSurface = false;
            int attempts = 0;

            //place biome exactly on the surface
            while (!foundSurface && attempts++ < 100000)
            {
                while (!WorldGen.SolidTile(PositionX, PositionY) && PositionY <= Main.worldSurface)
				{
					PositionY++;
				}
                if (!Main.tile[PositionX, PositionY].HasTile)
                {
					continue;
				}

                foundSurface = true;
            }

            for (int ClearX = XStart; ClearX <= XEdge; ClearX++)
            {
                for (int ClearY = (int)Main.worldSurface - ClearHeight; ClearY <= PositionY - 30; ClearY++)
                {
                    Tile tile = Framing.GetTileSafely(ClearX, ClearY);
                    tile.ClearEverything();
                    tile.LiquidType = 0;
                }
            }

            for (int X = XStart; X <= XEdge; X++)
            {
                for (int Y = PositionY - 60; Y <= Main.worldSurface; Y++)
                {
                    //place dirt blobs on non forest tiles
                    if (Main.tile[X, Y].TileType != TileID.Dirt && Main.tile[X, Y].TileType != TileID.Grass && Main.tile[X, Y].HasTile)
                    {
                        SpookyWorldMethods.TileRunner(X, Y, WorldGen.genRand.Next(15, 20), 1, TileID.Dirt, 
                        WallID.DirtUnsafe3, WallID.DirtUnsafe3, true, 0f, 0f, true, true, true);
                    }
                    //place walls
                    if (Main.tile[X, Y].WallType > 0)
                    {
                        WorldGen.KillWall(X, Y);
                        Main.tile[X, Y + 5].WallType = WallID.Dirt;
                        WorldGen.PlaceTile(X, Y, TileID.Dirt);
                    }
                }

                for (int FillY = (int)Main.worldSurface - 50; FillY <= Main.worldSurface; FillY++)
                {
                    if (Main.tile[X, FillY].HasTile)
                    {
                        SpookyWorldMethods.Circle(X, FillY, 5, TileID.Dirt, false);
                    }
                }
            }
        }

        private void PlaceGravestones(GenerationProgress progress, GameConfiguration configuration)
        {
            int XStart = PositionX;
            int XEdge = XStart + BiomeWidth;

            //place ambient objects
            for (int X = XStart - 25; X <= XEdge + 25; X++)
			{
                for (int Y = PositionY - 75; Y <= Main.worldSurface; Y++)
                {  
                    if (Main.tile[X, Y].TileType == TileID.Dirt || Main.tile[X, Y].TileType == TileID.Grass)
                    {
                        if (WorldGen.genRand.Next(3) == 0)
                        {
                            WorldGen.PlaceObject(X, Y - 1, TileID.Tombstones, true, Main.rand.Next(0, 5));
                        }
                    }
                }
            }
        }

        private void PlaceStructures(int X, int Y, int[,] BlocksArray, int[,] ObjectArray)
        {
            for (int PlaceX = 0; PlaceX < BlocksArray.GetLength(1); PlaceX++)
            {
                for (int PlaceY = 0; PlaceY < BlocksArray.GetLength(0); PlaceY++)
                {
                    int StructureX = X + PlaceX;
                    int StructureY = Y + PlaceY;

                    if (WorldGen.InWorld(StructureX, StructureY, 30))
                    {
                        Tile tile = Framing.GetTileSafely(StructureX, StructureY);
                        switch (BlocksArray[PlaceY, PlaceX])
                        {
                            //dont touch
                            case 0:
                            {
                                break;
                            }
                            //clear everything
                            case 1:
                            {
                                tile.ClearEverything();
                                WorldGen.KillTile(StructureX, StructureY);
                                break;
                            }
                            //catacomb brick
                            case 2:
                            {
                                tile.ClearEverything();
                                WorldGen.PlaceTile(StructureX, StructureY, ModContent.TileType<CatacombBrick>());
                                break;
                            }
                            //mossy catacomb brick
                            case 3:
                            {
                                tile.ClearEverything();
					            WorldGen.PlaceTile(StructureX, StructureY, ModContent.TileType<CatacombBrickMoss>());
                                break;
                            }
                            //stone blocks
                            case 4:
                            {
                                tile.ClearEverything();
					            WorldGen.PlaceTile(StructureX, StructureY, ModContent.TileType<SpookyWood>());
                                break;
                            }
                            //wood platforms and stone wall
                            case 5:
                            {
                                tile.ClearEverything();
					            WorldGen.PlaceTile(StructureX, StructureY, TileID.Platforms);
                                WorldGen.PlaceWall(StructureX, StructureY, WallID.Stone);
                                break;
                            }
                            //stone walls
                            case 6:
                            {
                                tile.ClearEverything();
					            WorldGen.PlaceWall(StructureX, StructureY, WallID.Stone);
                                break;
                            }
                            //wrought iron fence
                            case 7:
                            {
                                tile.ClearEverything();
                                WorldGen.PlaceWall(StructureX, StructureY, WallID.WroughtIronFence);
                                break;
                            }
                            //dirt
                            case 8:
                            {
                                tile.ClearEverything();
                                WorldGen.PlaceTile(StructureX, StructureY, TileID.Dirt);
                                break;
                            }
                            //chain
                            case 9:
                            {
                                tile.ClearEverything();
                                WorldGen.PlaceTile(StructureX, StructureY, TileID.Chain);
                                break;
                            }
                        }
                    }
                }
            }

            for (int PlaceX = 0; PlaceX < ObjectArray.GetLength(1); PlaceX++)
            {
                for (int PlaceY = 0; PlaceY < ObjectArray.GetLength(0); PlaceY++)
                {
                    int StructureX = X + PlaceX;
                    int StructureY = Y + PlaceY;

                    if (WorldGen.InWorld(StructureX, StructureY, 30))
                    {
                        Tile tile = Framing.GetTileSafely(StructureX, StructureY);
                        switch (ObjectArray[PlaceY, PlaceX])
                        {
                            //dont touch
                            case 0:
                            {
                                break;
                            }
                            //cobwebs
                            case 1:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceTile(StructureX, StructureY, TileID.Cobweb);
                                break;
                            }
                            //latern
                            case 2:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceObject(StructureX, StructureY, 42, true, 6);
                                break;
                            }
                            //lamp post
                            case 3:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceObject(StructureX, StructureY, TileID.Lampposts);
                                break;
                            }
                            //stone brick door
                            case 4:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceObject(StructureX, StructureY, TileID.ClosedDoor, true, 44);
                                break;
                            }
                            //gothic tables
                            case 5:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceObject(StructureX, StructureY, TileID.Tables, true, 14);
                                break;
                            }
                            //skull candle
                            case 6:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceObject(StructureX, StructureY, TileID.SkullLanterns);
                                break;
                            }
                            //casket
                            case 7:
                            {
                                tile.ClearTile();
                                ushort[] Caskets = new ushort[] { (ushort)ModContent.TileType<Casket1>(), 
                                (ushort)ModContent.TileType<Casket2>(), (ushort)ModContent.TileType<Casket3>() };
                                WorldGen.PlaceObject(StructureX, StructureY, WorldGen.genRand.Next(Caskets));
                                break;
                            }
                            //trash can (TODO: change this to custom chest)
                            case 8:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceObject(StructureX, StructureY, TileID.Containers, true, 6);
                                break;
                            }
                        }
                    }
                }
            }
        }

