        //place a giant dirt area for the graveyard to generate on
        private void PlaceCemeteryArea(GenerationProgress progress, GameConfiguration configuration)
        {
            progress.Message = "Generating burial grounds";

            Catacombs.PositionY = (int)Main.worldSurface - (Main.maxTilesY / 8);
         
            int XAmount = 0;

            //place biome based on opposite dungeon side
            if (WorldGen.dungeonSide == -1)
			{
                Catacombs.PositionX = Main.maxTilesX - (780 + 75);
			}
			else
			{
                Catacombs.PositionX = 400;
            }

            int XStart = Catacombs.PositionX;
            int XMiddle = XStart + (Catacombs.BiomeWidth / 2);
            int XEdge = XStart + Catacombs.BiomeWidth;

            bool foundSurface = false;
            int attempts = 0;

            //place biome exactly on the surface
            while (!foundSurface && attempts++ < 100000)
            {
                while (!WorldGen.SolidTile(XMiddle, Catacombs.PositionY) && Catacombs.PositionY <= Main.worldSurface)
				{
					Catacombs.PositionY++;
				}
                if (!Main.tile[XMiddle, Catacombs.PositionY].HasTile)
                {
					continue;
				}

                foundSurface = true;
            }

            for (int X = XMiddle - (Catacombs.BiomeWidth / 2); X <= XMiddle + (Catacombs.BiomeWidth / 2); X++)
            {
                for (int Y = Catacombs.PositionY - 75; Y <= Main.worldSurface; Y++)
                {
                    //place dirt blobs
                    if (Main.tile[X, Y].HasTile && Main.tile[X, Y].TileType != TileID.Cloud && Main.tile[X, Y].TileType != TileID.RainCloud)
                    {
                        SpookyWorldMethods.TileRunner(X, Y, WorldGen.genRand.Next(15, 20), 1, ModContent.TileType<CemeteryDirt>(), 
                        WallID.Cave3Unsafe, WallID.Cave3Unsafe, true, 0f, 0f, true, false, false, true, false);
                    }

                    //place small blob where walls exist to prevent unwanted craters or caves
                    if (Main.tile[X, Y].WallType > 0 && !Main.tile[X, Y].HasTile)
                    {
                        WorldGen.PlaceTile(X, Y, ModContent.TileType<CemeteryDirt>());
                    }
                }

                //fill in right above the world surface to prevent weird holes that just get stopped by the catacombs
                for (int FillY = (int)Main.worldSurface - 45; FillY <= Main.worldSurface; FillY++)
                {
                    SpookyWorldMethods.Circle(X, FillY, 10, ModContent.TileType<CemeteryDirt>(), false);
                }
            }

            //place clumps of stone
            for (int i = 0; i < (int)((double)(Main.maxTilesX * Main.maxTilesY * 27) * 1E-04); i++)
            {
                int X = WorldGen.genRand.Next(0, Main.maxTilesX);
                int Y = WorldGen.genRand.Next(0, Main.maxTilesY - 2);

                if (Main.tile[X, Y] != null && Main.tile[X, Y].HasTile)
                {
                    if (Main.tile[X, Y].TileType == ModContent.TileType<CemeteryDirt>())
                    {
                        WorldGen.TileRunner(X, Y, WorldGen.genRand.Next(10, 18), WorldGen.genRand.Next(10, 18), 
                        ModContent.TileType<CemeteryStone>(), false, 0f, 0f, false, true);
                    }
                }
            }
        }

        private void SpreadCemeteryGrass(GenerationProgress progress, GameConfiguration configuration)
        {
            int XStart = Catacombs.PositionX;
            int XMiddle = XStart + (Catacombs.BiomeWidth / 2);
            int XEdge = XStart + Catacombs.BiomeWidth;

            //spread grass on all cemetery dirt tiles
            for (int X = XMiddle - (Catacombs.BiomeWidth / 2) - 100; X <= XMiddle + (Catacombs.BiomeWidth / 2) + 100; X++)
            {
                for (int Y = Catacombs.PositionY - 75; Y <= Main.worldSurface; Y++)
                {
                    Tile up = Main.tile[X, Y - 1];
                    Tile down = Main.tile[X, Y + 1];
                    Tile left = Main.tile[X - 1, Y];
                    Tile right = Main.tile[X + 1, Y];

                    if (Main.tile[X, Y].TileType == ModContent.TileType<CemeteryDirt>() &&
                    ((!up.HasTile || up.TileType == TileID.Trees) || !down.HasTile || !left.HasTile || !right.HasTile))
                    {
                        Main.tile[X, Y].TileType = (ushort)ModContent.TileType<CemeteryGrass>();
                    }

                    if (Main.tile[X, Y].TileType == ModContent.TileType<CemeteryDirt>() &&
                    (up.TileType == ModContent.TileType<CemeteryGrass>() || down.TileType == ModContent.TileType<CemeteryGrass>() ||
                    left.TileType == ModContent.TileType<CemeteryGrass>() || right.TileType == ModContent.TileType<CemeteryGrass>()))
                    {
                        WorldGen.SpreadGrass(X, Y, ModContent.TileType<CemeteryDirt>(), ModContent.TileType<CemeteryGrass>(), false);
                    }

                    if (Main.tile[X, Y].TileType == ModContent.TileType<CemeteryDirt>() || Main.tile[X, Y].TileType == ModContent.TileType<CemeteryGrass>())
                    {
                        if (WorldGen.genRand.Next(12) == 0)
                        {
                            ushort[] Bushes = new ushort[] { (ushort)ModContent.TileType<CemeteryBush1>(), (ushort)ModContent.TileType<CemeteryBush2>() };

                            WorldGen.PlaceObject(X, Y - 1, WorldGen.genRand.Next(Bushes));
                        }
                    }
                }
            }
        }

        private void GrowCemeteryTrees(GenerationProgress progress, GameConfiguration configuration)
        {
            int XStart = Catacombs.PositionX;
            int XMiddle = XStart + (Catacombs.BiomeWidth / 2);
            int XEdge = XStart + Catacombs.BiomeWidth;

            //spread grass on all cemetery dirt tiles
            for (int X = XMiddle - (Catacombs.BiomeWidth / 2) - 100; X <= XMiddle + (Catacombs.BiomeWidth / 2) + 100; X++)
            {
                for (int Y = Catacombs.PositionY - 75; Y <= Main.worldSurface; Y++)
                {
                    if (Main.tile[X, Y].TileType == (ushort)ModContent.TileType<CemeteryGrass>())
                    {
                        WorldGen.GrowTree(X, Y - 1);
                    }
                }
            }
        }

        public static void PlaceStructures(int X, int Y, int[,] BlocksArray, int[,] ObjectArray)
        {
            for (int PlaceX = 0; PlaceX < BlocksArray.GetLength(1); PlaceX++)
            {
                for (int PlaceY = 0; PlaceY < BlocksArray.GetLength(0); PlaceY++)
                {
                    int StructureX = X + PlaceX;
                    int StructureY = Y + PlaceY;

                    if (WorldGen.InWorld(StructureX, StructureY, 30))
                    {
                        Tile tile = Framing.GetTileSafely(StructureX, StructureY);
                        switch (BlocksArray[PlaceY, PlaceX])
                        {
                            //dont touch
                            case 0:
                            {
                                break;
                            }
                            //clear everything
                            case 1:
                            {
                                tile.ClearEverything();
                                WorldGen.KillWall(StructureX, StructureY + 1);
                                break;
                            }
                            //loose soil
                            case 2:
                            {
                                tile.ClearEverything();
                                WorldGen.PlaceTile(StructureX, StructureY, ModContent.TileType<CemeteryDirt>());
                                break;
                            }
                            //rocky soil
                            case 3:
                            {
                                tile.ClearEverything();
                                WorldGen.PlaceTile(StructureX, StructureY, ModContent.TileType<CemeteryStone>());
                                break;
                            }
                            //crypt brick
                            case 4:
                            {
                                tile.ClearEverything();

                                //chance to place mossy brick, otherwise place normal brick
                                if (WorldGen.genRand.Next(4) == 0)
                                {
                                    WorldGen.PlaceTile(StructureX, StructureY, ModContent.TileType<CemeteryBrickMoss>());
                                }
                                else
                                {
                                    WorldGen.PlaceTile(StructureX, StructureY, ModContent.TileType<CemeteryBrick>());
                                }
                                break;
                            }
                            //old wood
                            case 5:
                            {
                                tile.ClearEverything();
                                WorldGen.PlaceTile(StructureX, StructureY, ModContent.TileType<SpookyWood>());
                                break;
                            }
                            //old wood platform
                            case 6:
                            {
                                tile.ClearEverything();
                                WorldGen.PlaceTile(StructureX, StructureY, ModContent.TileType<OldWoodPlatform>());
                                break;
                            }
                            //old wood wall
                            case 7:
                            {
                                tile.ClearEverything();
                                WorldGen.PlaceWall(StructureX, StructureY, ModContent.WallType<SpookyWoodWall>());
                                WorldGen.PlaceWall(StructureX - 1, StructureY, ModContent.WallType<SpookyWoodWall>());
                                WorldGen.PlaceWall(StructureX + 1, StructureY, ModContent.WallType<SpookyWoodWall>());
                                WorldGen.PlaceWall(StructureX, StructureY - 1, ModContent.WallType<SpookyWoodWall>());
                                WorldGen.PlaceWall(StructureX, StructureY + 1, ModContent.WallType<SpookyWoodWall>());
                                break;
                            }
                            //wrought iron fence
                            case 8:
                            {
                                tile.ClearEverything();
                                WorldGen.PlaceWall(StructureX, StructureY, WallID.WroughtIronFence);
                                break;
                            }
                            //water
                            case 9:
                            {
                                tile.ClearEverything();
                                tile.LiquidType = LiquidID.Water;
					            tile.LiquidAmount = 255;
                                break;
                            }
                        }
                    }
                }
            }

            for (int PlaceX = 0; PlaceX < ObjectArray.GetLength(1); PlaceX++)
            {
                for (int PlaceY = 0; PlaceY < ObjectArray.GetLength(0); PlaceY++)
                {
                    int StructureX = X + PlaceX;
                    int StructureY = Y + PlaceY;

                    if (WorldGen.InWorld(StructureX, StructureY, 30))
                    {
                        Tile tile = Framing.GetTileSafely(StructureX, StructureY);
                        switch (ObjectArray[PlaceY, PlaceX])
                        {
                            //dont touch
                            case 0:
                            {
                                break;
                            }
                            //cobwebs
                            case 1:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceTile(StructureX, StructureY, TileID.Cobweb);
                                break;
                            }
                            //wooden beams
                            case 2:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceTile(StructureX, StructureY, TileID.WoodenBeam);
                                break;
                            }
                            //chains
                            case 3:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceTile(StructureX, StructureY, TileID.Chain);
                                break;
                            }
                            //tombstones
                            case 4:
                            {
                                tile.ClearTile();
                                ushort[] Tombstones = new ushort[] { (ushort)ModContent.TileType<Tombstone1>(), (ushort)ModContent.TileType<Tombstone2>(), 
                                (ushort)ModContent.TileType<TombstoneCracked1>(), (ushort)ModContent.TileType<TombstoneCracked2>() };
                                WorldGen.PlaceObject(StructureX, StructureY, WorldGen.genRand.Next(Tombstones));
                                break;
                            }
                            //burial caskets
                            case 5:
                            {
                                tile.ClearTile();
                                ushort[] Caskets = new ushort[] { (ushort)ModContent.TileType<BurialCasket1>(), (ushort)ModContent.TileType<BurialCasket2>() };
                                WorldGen.PlaceObject(StructureX, StructureY, WorldGen.genRand.Next(Caskets));
                                break;
                            }
                            //little bone statue
                            case 6:
                            {
                                tile.ClearTile();
                                ushort[] Statue = new ushort[] { (ushort)ModContent.TileType<LittleBoneStatue1>(), (ushort)ModContent.TileType<LittleBoneStatue2>() };
                                WorldGen.PlaceObject(StructureX, StructureY, WorldGen.genRand.Next(Statue));
                                break;
                            }
                            //big bone statue
                            case 7:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceTile(StructureX, StructureY, ModContent.TileType<BigBoneStatue>());
                                break;
                            }
                            //cemetery lamps
                            case 8:
                            {
                                tile.ClearTile();
                                ushort[] Lamps = new ushort[] { (ushort)ModContent.TileType<CemeteryLamp1>(), (ushort)ModContent.TileType<CemeteryLamp2>() };
                                WorldGen.PlaceObject(StructureX, StructureY, WorldGen.genRand.Next(Lamps));
                                break;
                            }
                            //chest (chest itself is not done, leave blank for now)
                            case 9:
                            {
                                tile.ClearTile();
                                break;
                            }
                        }
                    }
                }
            }
        }

