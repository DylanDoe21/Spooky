                                (ushort)ModContent.TileType<StatueSkull>(), (ushort)ModContent.TileType<StatueSoul>() };
                                WorldGen.PlaceObject(StructureX, StructureY, Pillars[PillarType]);
                                break;
                            }
                            //burial piles
                            case 6:
                            {
                                tile.ClearTile();
                                ushort[] Piles = new ushort[] { (ushort)ModContent.TileType<BurialPile1>(),
                                (ushort)ModContent.TileType<BurialPile2>(), (ushort)ModContent.TileType<BurialPile3>() };
