        private void PlaceCatacombRoom(int X, int Y, int[,] BlocksArray, int[,] ObjectArray, int PillarType)
        {
            for (int PlaceX = 0; PlaceX < BlocksArray.GetLength(1); PlaceX++)
            {
                for (int PlaceY = 0; PlaceY < BlocksArray.GetLength(0); PlaceY++)
                {
                    int StructureX = X + PlaceX;
                    int StructureY = Y + PlaceY;

                    if (WorldGen.InWorld(StructureX, StructureY, 30))
                    {
                        Tile tile = Framing.GetTileSafely(StructureX, StructureY);
                        switch (BlocksArray[PlaceY, PlaceX])
                        {
                            //clear blocks
                            case 0:
                            {
                                tile.ClearTile();
                                WorldGen.KillTile(StructureX, StructureY);
                                break;
                            }
                            //catacomb brick
                            case 1:
                            {
                                if (tile.TileType != ModContent.TileType<CatacombBrickMoss>() && tile.HasTile)
                                {
                                    tile.ClearTile();
                                    WorldGen.PlaceTile(StructureX, StructureY, ModContent.TileType<CatacombBrick>());
                                }
                                break;
                            }
                            //catacomb tiles
                            case 2:
                            {
                                if (tile.TileType != ModContent.TileType<CatacombBrickMoss>())
                                {
                                    tile.ClearTile();
                                    WorldGen.PlaceTile(StructureX, StructureY, ModContent.TileType<CatacombTiles>());
                                }
                                break;
                            }
                            //platform
                            case 3:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceObject(StructureX, StructureY, TileID.Platforms, true, 12);
                                break;
                            }
                            //dirt
                            case 4:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceTile(StructureX, StructureY, TileID.Dirt);
                                break;
                            }
                            //cobwebs
                            case 5:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceTile(StructureX, StructureY, TileID.Cobweb);
                                break;
                            }
                            //lava
                            case 6:
                            {
                                tile.ClearTile();
                                tile.LiquidType = LiquidID.Lava;
					            tile.LiquidAmount = 255;
                                break;
                            }
                        }
                    }
                }
            }

            for (int PlaceX = 0; PlaceX < ObjectArray.GetLength(1); PlaceX++)
            {
                for (int PlaceY = 0; PlaceY < ObjectArray.GetLength(0); PlaceY++)
                {
                    int StructureX = X + PlaceX;
                    int StructureY = Y + PlaceY;

                    if (WorldGen.InWorld(StructureX, StructureY, 30))
                    {
                        Tile tile = Framing.GetTileSafely(StructureX, StructureY);
                        switch (ObjectArray[PlaceY, PlaceX])
                        {
                            //dont touch
                            case 0:
                            {
                                break;
                            }
                            //catacomb lantern
                            case 1:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceObject(StructureX, StructureY + 1, ModContent.TileType<CatacombLantern>());
                                break;
                            }
                            //casket
                            case 2:
                            {
                                tile.ClearTile();
                                ushort[] Caskets = new ushort[] { (ushort)ModContent.TileType<Casket1>(), (ushort)ModContent.TileType<Casket2>(), 
                                (ushort)ModContent.TileType<Casket3>(), (ushort)ModContent.TileType<Casket4>() };
                                WorldGen.PlaceObject(StructureX, StructureY, WorldGen.genRand.Next(Caskets));
                                break;
                            }
                            //skull lamp
                            case 3:
                            {
                                tile.ClearTile();
                                WorldGen.PlaceObject(StructureX, StructureY, ModContent.TileType<SkullLamp>());
                                break;
                            }
                            //coffins
                            case 4:
                            {
                                tile.ClearTile();
                                ushort[] Coffins = new ushort[] { (ushort)ModContent.TileType<Coffin1>(),
                                (ushort)ModContent.TileType<Coffin2>(), (ushort)ModContent.TileType<Coffin3>() };
                                WorldGen.PlaceObject(StructureX, StructureY, WorldGen.genRand.Next(Coffins));
                                break;
